package com.loop;

public class ForLoopDemo {

    public static void main(String[] args) {

        for (int i = 1; i <= 5; i++) {
            System.out.println(i + " " + "ios");
        }
        for (int i = 0; i <= 5; i++) {
            System.out.println(i + " " + "Siri");
        }
        for (int i = 5; i >= 0; i--) {
            System.out.println(i + " " + "WWDC");
        }
    }

}
